package cn.traffic.manage.ss.entity;

public class RoadCross {
	private String frName;
	private String trName;
	private Integer lineNum;
	private Integer bsNum;
	private Integer busNum;
	
	
	public String getFrName() {
		return frName;
	}
	public void setFrName(String frName) {
		this.frName = frName;
	}
	public String getTrName() {
		return trName;
	}
	public void setTrName(String trName) {
		this.trName = trName;
	}
	public Integer getLineNum() {
		return lineNum;
	}
	public void setLineNum(Integer lineNum) {
		this.lineNum = lineNum;
	}
	public Integer getBsNum() {
		return bsNum;
	}
	public void setBsNum(Integer bsNum) {
		this.bsNum = bsNum;
	}
	public Integer getBusNum() {
		return busNum;
	}
	public void setBusNum(Integer busNum) {
		this.busNum = busNum;
	}
	
}
